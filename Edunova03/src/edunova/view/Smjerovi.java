    private void ucitajSmjerove() {
        
        DefaultListModel<Smjer> m = new DefaultListModel<>();
        for(Smjer s : obradaSmjer.read()){
            m.addElement(s);
        }
        lstSmjerovi.setModel(m);
        
        
    }

    private void ocistiPolja() {
       txtCijena.setText("");
       txtNaziv.setText("");
       txtTrajanje.setText("");
       txtUpisnina.setText("");
       chbVerificiran.setSelected(false);
    }

    private boolean kontrola(Smjer s) {
         s.setNaziv(txtNaziv.getText());
        try {
            s.setTrajanje(Integer.parseInt(txtTrajanje.getText()));
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Trajanje nije broj");
            return false;
        }
        
        try {
            s.setCijena(
                    new BigDecimal(
                            format.parse(txtCijena.getText()).floatValue()
                    )
            );
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showConfirmDialog(null, "Cijena nije broj");
            return false;
        }
        
        try {
            s.setUpisnina(
                    new BigDecimal(
                            format.parse(txtUpisnina.getText()).floatValue()
                    )
            );
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Upisnina nije broj");
            return false;
        }
        
        s.setVerificiran(chbVerificiran.isSelected());
        
        
        return true;
    }
