    private void lstSmjeroviValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstSmjeroviValueChanged
        if(evt.getValueIsAdjusting()){
            return;
        }
        
        Smjer s = lstSmjerovi.getSelectedValue();
        if(s==null){
            return;
        }
        
        txtNaziv.setText(s.getNaziv());
        txtTrajanje.setText(String.valueOf(s.getTrajanje()));
        txtCijena.setText(format.format(s.getCijena()));
        txtUpisnina.setText(format.format(s.getUpisnina()));
        
        chbVerificiran.setSelected(s.isVerificiran());
        
    }//GEN-LAST:event_lstSmjeroviValueChanged

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
       Smjer s = new Smjer();
      
       if(!kontrola(s)){
           return;
       }
        
        try {
            obradaSmjer.create(s);
        } catch (EdunovaException e) {
            JOptionPane.showConfirmDialog(null, e.getPoruka());
            return;
        }
        
        ucitajSmjerove();
        
        ocistiPolja();
        
        
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnPromjenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjenaActionPerformed
        Smjer s = lstSmjerovi.getSelectedValue();
        
        if(s==null){
            JOptionPane.showConfirmDialog(null, "Prvo odaberite smjer");
        }
        
        
         if(!kontrola(s)){
           return;
       }
        
        try {
            obradaSmjer.update(s);
        } catch (EdunovaException e) {
            JOptionPane.showConfirmDialog(null, e.getPoruka());
            return;
        }
        
        ucitajSmjerove();
        
        ocistiPolja();
        
    }//GEN-LAST:event_btnPromjenaActionPerformed

    private void btnBrisanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrisanjeActionPerformed
        Smjer s = lstSmjerovi.getSelectedValue();
        
        if(s==null){
            JOptionPane.showConfirmDialog(null, "Prvo odaberite smjer");
        }
        
        if(obradaSmjer.delete(s)){
            ucitajSmjerove();
            ocistiPolja();
        }else{
            JOptionPane.showConfirmDialog(null,"Smjer nije obrisan jer ima grupe");
        }
        
    }//GEN-LAST:event_btnBrisanjeActionPerformed

  

